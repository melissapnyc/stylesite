function make_list() { //find selected details   

    for (var i = 1; i <= 3; i++) {
        if (document.getElementById("radio"+i).checked) {
            time = document.getElementById("radio"+i).value;
        }
    };        
    for (var i = 4; i <= 6; i++) {
        if (document.getElementById("radio"+i).checked) {
            length = document.getElementById("radio"+i).value;
        }
    };  
    for (var i = 7; i <= 9; i++) {
        if (document.getElementById("radio"+i).checked) {
            texture = document.getElementById("radio"+i).value;
        }
    };  
    for (var i = 10; i <= 12; i++) {
        if (document.getElementById("radio"+i).checked) {
            type = document.getElementById("radio"+i).value;
        }
    };  
    
    return [time, length, texture, type];
}

function search() {
    var image_list = [];   
    var details = make_list();
    for (var i=0; i < full_list.length; i++) {
        if (details[0] == full_list[i][1] && details[1] == full_list[i][2] && details[2] == full_list[i][3] && details[3] == full_list[i][4]) {
            image_list.push(full_list[i][5])
        }
    };
    if (image_list.length >= 1) {
        image = image_list.pop()
        var page_display = ("<button onclick='next("+ [(JSON.stringify(image_list))] +")'><img src='" + image + "'></button>");
        document.getElementById("t2").innerHTML=page_display;
    }
    else {
        sorry()
    }
}

